# ============================================================================
# 插值计算函数
# ============================================================================

def cal_interplot(x1: float, y1: float, x2: float, y2: float, now_x: float) -> float:
    """两点线性插值。

    Args:
        x1, y1: 左侧已知点
        x2, y2: 右侧已知点
        now_x: 待插值的 x 坐标

    Returns:
        now_x 对应的线性插值 y 值
    """
    return y1 + (y2 - y1) * (now_x - x1) / (x2 - x1)


def cal_now_y_in_xy_list(xy_list: list, now_x: float) -> float:
    """在一维 [(x, y), ...] 列表中插值。

    边界处理:
      - now_x <= 最小 x → 返回最小 y
      - now_x >= 最大 x → 按比例外推 (y * now_x / max_x)

    Args:
        xy_list: 已排序的 [(x, y), ...] 列表
        now_x: 待插值的 x 值

    Returns:
        插值得到的 y 值
    """
    if now_x <= xy_list[0][0]:
        return xy_list[0][1]
    if now_x >= xy_list[-1][0]:
        return xy_list[-1][1] * now_x / xy_list[-1][0]
    for i in range(len(xy_list)):
        if xy_list[i][0] >= now_x:
            assert i > 0
            return cal_interplot(
                xy_list[i - 1][0], xy_list[i - 1][1],
                xy_list[i][0], xy_list[i][1], now_x
            )
    raise Exception("Unreachable in cal_now_y_in_xy_list")


def cal_now_y_in_x_xy_list_list(x_xy_list_list: list, outer_x: float, inner_x: float) -> float:
    """在二维 [(outer_x, [(inner_x, y), ...]), ...] 列表中双线性插值。

    先在外层 (outer_x) 插值，再在内层 (inner_x) 插值。
    用于 decode 阶段: outer_x = context_length, inner_x = batch_size。

    Args:
        x_xy_list_list: [(context_len, [(batch, ms), ...]), ...]
        outer_x: context_length
        inner_x: batch_size

    Returns:
        插值得到的耗时 (毫秒)
    """
    if outer_x <= x_xy_list_list[0][0]:
        return cal_now_y_in_xy_list(x_xy_list_list[0][1], inner_x)
    if outer_x >= x_xy_list_list[-1][0]:
        return cal_now_y_in_xy_list(x_xy_list_list[-1][1], inner_x) * outer_x / x_xy_list_list[-1][0]
    for i in range(len(x_xy_list_list)):
        if x_xy_list_list[i][0] >= outer_x:
            assert i > 0
            lft_y = cal_now_y_in_xy_list(x_xy_list_list[i - 1][1], inner_x)
            rgt_y = cal_now_y_in_xy_list(x_xy_list_list[i][1], inner_x)
            return cal_interplot(
                x_xy_list_list[i - 1][0], lft_y,
                x_xy_list_list[i][0], rgt_y, outer_x
            )
    raise Exception("Unreachable in cal_now_y_in_x_xy_list_list")


