def estimate_duration(model_type: str, node_type: str, node_label: str,
                      avg_input_length: float, batch_size: int,
                      stage_type: str) -> float:
    """估算推理耗时（秒）。

    基于预 profiling 数据，通过插值计算给定配置下的推理耗时。
    已内置 safety_factor 用于补偿 profiling 数据与实际运行之间的差异。

    Args:
        model_type: 模型类型，如 'llama-2-7b'
        node_type: 节点类型，'cpu' 或 'gpu'
        node_label: 节点标签，如 'cpu', 'gpu', 'aliyun-16c', 'aliyun-8c'
        avg_input_length: prefill 的平均输入长度；decode 的平均上下文长度
        batch_size: decode 阶段的 batch 大小（prefill 时忽略）
        stage_type: 'prefill' 或 'decode'

    Returns:
        估算耗时（秒）

    Safety factors:
      - GPU: 1.05
      - CPU (标准): 1.1
      - CPU (aliyun decode): 1.5 (更保守)
    """
    # 安全系数: 补偿 profiling 时的理想环境与实际部署之间的差距
    safety_factor = 1.05
    if node_type == 'cpu':
        safety_factor = 1.1
        if stage_type == 'decode':
            if '8c' in node_label or '16c' in node_label:
                safety_factor = 1.5

    if stage_type == 'prefill':
        return (cal_now_y_in_xy_list(
            models_power[model_type][node_type][node_label]['prefill'],
            avg_input_length
        ) * safety_factor / 1000.0)
    elif stage_type == 'decode':
        return (cal_now_y_in_x_xy_list_list(
            models_power[model_type][node_type][node_label][stage_type],
            avg_input_length, batch_size
        ) * safety_factor / 1000.0)
    else:
        raise ValueError(f"Unknown stage_type: {stage_type}")


def get_serverlessllm_concurrency(model_type: str, node_type: str,
                                   node_label: str, sllm_max_shares: int) -> Optional[int]:
    """获取 ServerlessLLM 模式下的并发限制。

    Args:
        model_type: 模型类型
        node_type: 节点类型
        node_label: 节点标签
        sllm_max_shares: 最大共享数 (1=单租户, ≥2=多租户)

    Returns:
        该配置下允许的最大并发数；如果未配置则返回 None
    """
    assert sllm_max_shares > 0
    if sllm_max_shares == 1:
        return models_power[model_type][node_type][node_label]['sllm_concurrency']
    else:
        return models_power[model_type][node_type][node_label]['sllm_share_concurrency'][sllm_max_shares]
